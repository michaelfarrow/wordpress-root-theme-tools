function root_iframe_content($content) {
    $content = preg_replace('#<iframe.+?src=".+?youtube.com/embed/([a-zA-Z0-9_-]{11})["?][^>]+?></iframe>#i', 'https://www.youtube.com/watch?v=$1', $content);
    $content = preg_replace('#<iframe.+?src=".+?vimeo.com/video/(\d+)["?][^>]+?></iframe>#i', 'http://vimeo.com/$1', $content);
 
    return $content;
}
 
add_filter('the_content', 'root_iframe_content', 1);

